    private void btnClosedRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClosedRequestActionPerformed
        // TODO add your handling code here:
        int row = tblWorkRequest.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select an entry form the table", "Information", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        AddInventoryWorkRequest aiwr = (AddInventoryWorkRequest) tblWorkRequest.getValueAt(row, 0);
        if (aiwr.getStatus().equals("Closed")) {
            JOptionPane.showMessageDialog(null, "This request has already been closed");
            return;
        }
        
        if(aiwr.getStatus().equals("Completed"))
        {
        aiwr.setStatus("Closed");
        JOptionPane.showMessageDialog(null, "The request has been closed succesfully");

        JOptionPane.showMessageDialog(null, "Updating inventory");

        ArrayList<DrugItem> drugItemList = aiwr.getDrugItemList();

        updateInventory(drugItemList);
        populateDrugRequest();

        }
        else
        {
        JOptionPane.showMessageDialog(null, "The request has not yet been completed");
        return;
        }
    }//GEN-LAST:event_btnClosedRequestActionPerformed

