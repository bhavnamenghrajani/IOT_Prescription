    private void btnShowQuantityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowQuantityActionPerformed
        // TODO add your handling code here:

        int row = tblWorkRequest.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select an entry form the table", "Information", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        AddInventoryWorkRequest aiwr = (AddInventoryWorkRequest) tblWorkRequest.getValueAt(row, 0);

        DefaultTableModel dtm = (DefaultTableModel) tblDrugItem.getModel();
        dtm.setRowCount(0);
        for (DrugItem drugItem : aiwr.getDrugItemList()) {

            Object r[] = new Object[3];
            r[0] = drugItem.getDrug().getDrugId();
            r[1] = drugItem.getDrug().getDrugName();
            r[2] = drugItem.getQuantity();
            dtm.addRow(r);

        }


    }//GEN-LAST:event_btnShowQuantityActionPerformed

    public void backAction() {
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);

    }

    private void updateInventory(ArrayList<DrugItem> drugItemList) {

        boolean found = false;
        for (DrugItem drugItem : drugItemList) {
            found = false;
            for (DrugItem di : drugInventory.getDrugInventory()) {
                if (drugItem.getDrug() == di.getDrug()) {
                    found = true;
                    di.setQuantity(di.getQuantity() + drugItem.getQuantity());
                    JOptionPane.showMessageDialog(null, "Inventory has been added");
                    break;
                }
            }
            if (!found) {
                System.out.println("Not Found");
                drugInventory.getDrugInventory().add(drugItem);
                found = false;
            }

        }

    }


