    public void populatePharmaComboBox() {
        pharmacyJComboBox.removeAllItems();
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
            if (enterprise instanceof PharmacyEnterprise) {

                for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    pharmacyJComboBox.addItem(organization);

                }

            }
        }

    }

    public void populateDrugContents() {
        DefaultTableModel dtm = (DefaultTableModel) tblDrugContents.getModel();
        dtm.setRowCount(0);

        for (DrugItems di : drugContents.getDrugItemList()) {

            if (di.getDrugItem() != null) {
                Object row[] = new Object[4];
                row[0] = di.getDrugItem().getDrug();
                row[1] = di.getDrugItem().getDrug().getDrugName();
                row[2] = di.getQuantity();
                row[3] = di.getTotalQuantity();
                dtm.addRow(row);
            }
        }

    }

