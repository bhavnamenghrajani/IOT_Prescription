    private void btnWishListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnWishListActionPerformed
        // TODO add your handling code here:
        int row = tblDrugContents.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select an entry form the table", "Information", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        Drug drug = (Drug) tblDrugContents.getValueAt(row, 0);
        Object r[] = new Object[2];
        r[0] = drug;
        r[1] = drug.getDrugName();
        dtmWishList.addRow(r);

        DrugItem drugItem = new DrugItem();
        drugItem.setDrug(drug);
        drugItemList.add(drugItem);
        System.out.println("Drug added " + drugItem.getDrug());
    }//GEN-LAST:event_btnWishListActionPerformed

    private void btnRequestDrugsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestDrugsActionPerformed
        // TODO add your handling code here:
        Organization organization = (Organization) pharmacyJComboBox.getSelectedItem();
        organization.getWorkQueue().getWorkRequestList().add(addDrugWorkRequest);
        userAccount.getWorkQueue().getWorkRequestList().add(addDrugWorkRequest);
        addDrugWorkRequest.setMessage("Drugs required");
        addDrugWorkRequest.setStatus("Request initiated");
        addDrugWorkRequest.setSender(userAccount);
        addDrugWorkRequest.setRequestDate(new Date());
        addDrugWorkRequest.setDrugItemList(drugItemList);
        addDrugWorkRequest.setPrescription(prescription);

        JOptionPane.showMessageDialog(null, "Request sent succesfully to the pharmacist");

    }//GEN-LAST:event_btnRequestDrugsActionPerformed

